package com.javaandexcel.bean;

public class GeoPoint {

	public GeoPoint() {
		super();
	}

	private double latitudeE6;// 经度
	private double longitudeE6;// 纬度

	public double getLatitudeE6() {
		return latitudeE6;
	}

	public void setLatitudeE6(double latitudeE6) {
		this.latitudeE6 = latitudeE6;
	}

	public double getLongitudeE6() {
		return longitudeE6;
	}

	public void setLongitudeE6(double longitudeE6) {
		this.longitudeE6 = longitudeE6;
	}

	@Override
	public String toString() {
		return "latitudeE6=" + latitudeE6 + ", longitudeE6=" + longitudeE6;
	}

	public GeoPoint(double latitudeE6, double longitudeE6) {
		super();
		this.latitudeE6 = latitudeE6;
		this.longitudeE6 = longitudeE6;
	}

}
